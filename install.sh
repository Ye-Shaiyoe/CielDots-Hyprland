    local zshrc="$DOTFILES_DIR/.config/zsh/.zshrc"
    # If already patched, skip
    grep -q "emerge" "$zshrc" 2>/dev/null && return

    cat >> "$zshrc" << 'ZSHEOF'

# ── Gentoo / Portage aliases (auto-added by install.sh) ──
alias pkgi='sudo emerge --ask'
