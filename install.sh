step()  { echo -e "\n${M}══${NC} ${T}$*${NC}"; _log STEP "$*"; }

DOTFILES_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BACKUP_DIR="$HOME/.config-backup-cieldots/$(date '+%Y%m%d_%H%M%S')"
