progress() {
    CURRENT_STEP=$(( CURRENT_STEP + 1 ))
    local pct=$(( CURRENT_STEP * 100 / TOTAL_STEPS ))
    local filled=$(( pct / 5 ))
    local empty=$(( 20 - filled ))
    local bar
    bar="${M}$(printf '█%.0s' $(seq 1 $filled))${S}$(printf '░%.0s' $(seq 1 $empty))${NC}"
    printf "\r  [%s] ${T}%3d%%${NC} %s\n" "$bar" "$pct" "$1"
}

